// Declaración de variables 'a' y 'b'
let a, b;

// Asignación de valores a las variables
a = 5;
b = 4;

// Operaciones aritméticas básicas con los números
console.log(a + b); // Suma: 5 + 4 = 9
console.log(a - b); // Resta: 5 - 4 = 1
console.log(a / b); // División: 5 / 4 = 1.25
console.log(a * b); // Multiplicación: 5 * 4 = 20
